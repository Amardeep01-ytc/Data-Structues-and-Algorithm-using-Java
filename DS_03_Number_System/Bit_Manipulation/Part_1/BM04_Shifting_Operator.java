package DS_03_Number_System.Bit_Manipulation.Part_1;

public class BM04_Shifting_Operator {
    public static void main(String[] args) {
        System.out.println(13 >> 1);
        System.out.println(13 >> 2);
        System.out.println(13 >> 3);
        System.out.println(13 << 1);
        System.out.println(13 << 2);
    }
}
